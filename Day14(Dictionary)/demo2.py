# fetch keys from a dictionary

d1 = {'student':'vittal','email':'abc@gmail.com','course':'python'}
print(d1.keys())
# dict_keys(['student', 'email', 'course'])

# fetch values from a dictionary
print(d1.values())
# dict_values(['vittal', 'abc@gmail.com', 'python'])

# fetch all items from a dictionary - fetches keys and values together
print(d1.items())
# dict_items([('student', 'vittal'), ('email', 'abc@gmail.com'), ('course', 'python')])


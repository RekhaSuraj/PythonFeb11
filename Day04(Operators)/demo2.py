# Subtraction
a1 = 10
a2 = 15
print(a1-a2)
# -5

x = 30.50
y = 22.56
print(x-y)
# 7.940000000000001

# Multiplication
m1 = 50
m2 = 60
print(m1*m2)
# 3000

# Division
d1 = 45
d2 = 9
print(d1/d2)
# 5.0

# Floor division (//) - rounds the quotient to the nearest number (approximate value) 
# ignores the decimal points
f1 = 9
f2 = 2
f3 = f1//f2
print(f3)
# 4

# % modulus - fetches only the remainder
m1 = 18
m2 = 2
m3 = m1%m2
print(m3)
# 0 - remainder


m4 = 43
m5 = 3
print(m4%m5)
# 1

# Exponentiation (power of a number)
e1 = 4
e2 = 3
print(e1**e2)
# 64



# Conversion from decimal to binary
# Binary numbers are 0 and 1, it will have a prefix of 0b. This prefix indicates that it is a binary number
# Syntax : bin(number)

n1 = 17
print(bin(n1))
# 0b10001

n2 = 80
print(bin(n2))
# 0b1010000

print(bin(7))
# 0b111

# Conversion from binary to decimal
b1 = 0b10001
print(b1)
# 17

b2 = 0b111000
print(b2)
# 56






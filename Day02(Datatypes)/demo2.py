# Python has inbuilt Datatypes
# 1.Number/Numeric - Has 3 types int, float, complex
# 2.Boolean
# 3.Text datatype (String)
# 4.Sequential datatypes - list, tuple, set, frozenset, dictionary

# Numeric 
# int returns integer values like whole numbers 0,1,2,3,4 and -ve numbers also like -1,-2

var1 = 10
print('var1 value:',var1)
# var1 value: 10

print('type of var1',type(var1))
# <class 'int'>

var2 = 15
# print('var2 value:',var2)
# var2 value: 15

var3 = -5
print('var3 type',type(var3))
# var3 type <class 'int'>


# type() - inbuilt method that returns the datatype of the object
# syntax: type()

# float - numbers with decimal points datatype
# Ex: 3.25, 50.3,40.0
v1 = 15.25
# print('v1 is:',v1)

print('type of v1',type(v1))
# type of v1 <class 'float'>

v2 = 15.0
print('type of v2',type(v2))
# type of v2 <class 'float'>



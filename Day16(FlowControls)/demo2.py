# The if-else statement checks the condition and executes the if block of code when the condition is True,
# and if the condition is False, it will execute the else block of code.
#
# Syntax of the if-else statement
#
# if condition:
#     statement 1
# else:
#     statement 2
#
# If the condition is True, then statement 1 will be executed If the condition is False, statement 2 will be executed.
x = False

# if x: is same as if x == True
if x:
    print("Condition is True")
else:
    print('Condition is False')


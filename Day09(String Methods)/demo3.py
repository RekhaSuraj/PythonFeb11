
# isupper() - inbuilt method to check if a given string is in upper case
str1 = 'how are you?'
print(str1.isupper())
# False

# islower() - inbuilt method to check if a given string is in lower case
print(str1.islower())
# True

str2 = 'Welcome'
print(str2.isupper())
# False

print(str2.islower())
# False



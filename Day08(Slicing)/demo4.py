# String methods

str1 = 'Today is a good day'

# If we want to know information about any builtin methods, we use help()
# syntax help(str)

# Convert the string to upper case
print(str1.upper())
# TODAY IS A GOOD DAY

# Convert the string to lower case
print(str1.lower())
# today is a good day


# capitalize - changes the first letter of the string to capital
str2 = 'welcome'
print(str2.capitalize())
# Welcome

#The casefold() method returns a string where all the characters are lower case.
# sy:string.casefold()(is a strong and more Aggressive)
print(str1.casefold())
# today is a good day

# swapcase
str3 = 'Welcome to GRK'
print(str3.swapcase())
# wELCOME TO grk


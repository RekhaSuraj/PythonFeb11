# Membership operators (Relational operators)
# These are used on only iterables or collections (String, List, Tuple, set, frozenset, dictionary)
# There are 2 types - in , not in

list1 = ['Aparna','Ravishankar','Chandrakala','Deepak','Dheeraj']

# in - Returns True if an object is present
# print('Rekha' in list1)
# False

list2 = ['b','d','a','t']
list3 = ['k']
# print(list3 in list2)
# False

# not in - Returns True  if an object is not present
print('Rekha' not in list1)
# True

